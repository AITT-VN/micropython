import time
from machine import Pin

from setting import *

MAX_DISTANCE_CM = const(200) #cm
MAX_DISTANCE_MM = const(2000) #mm

class UltraSonic:
    """
    Driver to use the untrasonic sensor HC-SR04.
    The sensor range is between 2cm and 4m.

    The timeouts received listening to echo pin are converted to OSError('Out of range')

    """
    # echo_timeout_us is based in chip range limit (400cm)
    def __init__(self, port=1, echo_timeout_us=500*2*30):
        """
        trigger_pin: Output pin to send pulses
        echo_pin: Readonly pin to measure the distance. The pin should be protected with 1k resistor
        echo_timeout_us: Timeout in microseconds to listen to echo pin. 
        By default is based in sensor limit range (4m)
        """
        self.echo_timeout_us = echo_timeout_us
        self.reset_port(port)

    def reset_port(self, port):
        self.port = port
        # Grove port: GND VCC S2 (Echo) S1 (Trigger)
        trigger_pin = PORTS_DIGITAL[port][0]
        echo_pin = PORTS_DIGITAL[port][1]

        # Init trigger pin (out)
        self.trigger = Pin(trigger_pin, mode=Pin.OUT, pull=None)
        self.trigger.value(0)

        # Init echo pin (in)
        self.echo = Pin(echo_pin, mode=Pin.IN, pull=None)
    
    def _send_pulse_and_wait(self):
        """
        Send the pulse to trigger and listen on echo pin.
        We use the method `machine.time_pulse_us()` to get the microseconds until the echo is received.
        """
        self.trigger.value(0) # Stabilize the sensor
        time.sleep_us(5)
        self.trigger.value(1)
        # Send a 10us pulse.
        time.sleep_us(10)
        self.trigger.value(0)
        try:
            pulse_time = machine.time_pulse_us(self.echo, 1, self.echo_timeout_us)
            return pulse_time
        except OSError as ex:
            if ex.args[0] == 110: # 110 = ETIMEDOUT
                raise OSError('Out of range')
            raise ex

    def distance_mm(self, port=1):
        """
        Get the distance in milimeters without floating point operations.
        """
        if port != self.port:
            self.reset_port(port)

        pulse_time = self._send_pulse_and_wait()

        # To calculate the distance we get the pulse_time and divide it by 2 
        # (the pulse walk the distance twice) and by 29.1 becasue
        # the sound speed on air (343.2 m/s), that It's equivalent to
        # 0.34320 mm/us that is 1mm each 2.91us
        # pulse_time // 2 // 2.91 -> pulse_time // 5.82 -> pulse_time * 100 // 582 
        mm = pulse_time * 100 // 582
        if mm >= 0 and mm < MAX_DISTANCE_MM:
            return mm
        else:
            return MAX_DISTANCE_MM

    def distance_cm(self, port=1):
        """
        Get the distance in centimeters with floating point operations.
        It returns a float
        """
        if port != self.port:
            self.reset_port(port)

        pulse_time = self._send_pulse_and_wait()

        # To calculate the distance we get the pulse_time and divide it by 2 
        # (the pulse walk the distance twice) and by 29.1 becasue
        # the sound speed on air (343.2 m/s), that It's equivalent to
        # 0.034320 cm/us that is 1cm each 29.1us
        cms = (pulse_time / 2) / 29.1
        
        if cms >= 0 and cms < MAX_DISTANCE_CM:
            return cms
        else:
            return MAX_DISTANCE_CM

ultrasonic = UltraSonic()